package homework.day1.basetask;

public class TrainMethodsReturn {
    public int returnNewInt(int a) {
        return a * 3;
    }
        public long returnNewLong(long a) {
            return a - 4;
        }
    public float returnNewFloat(float a) {
        return a / 2;
    }
    public double returnNewDouble(double a) {
        return a + 8;
    }
    public short returnNewShort(short a) {
        return (short) (a - 1);
    }

    public byte returnNewByte(byte a) {
        return (byte) (a * 2);
    }

    public boolean returnNewBoolean(boolean a) {
        return !a;
    }
}
