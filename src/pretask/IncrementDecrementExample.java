package pretask;
// объявите целочисленную переменную и используйте операторы инкремента и декремента
// для изменения её значения, выведите результаты на экран;
public class IncrementDecrementExample {
    public static void main(String[] args) {
        int a = 8;
        System.out.println(++a + "," + --a);
        System.out.println(a++ + "," + a-- + "," + a);
    }
}
