//объявите целочисленную переменную и используйте условный оператор if,
// чтобы проверить, больше ли она 10, выведите результат проверки на экран
public class ConditionalExample {
    public static void main (String [] args) {
        int number = 8;
        if (number > 10) {
            System.out.println(number + " Больше 10");
        } else {
            System.out.println(number + " Меньше 10");
        }
    }
}
